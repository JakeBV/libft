#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *ptr;

	ptr = NULL;
	while (*s)
	{
		if (*(char*)s++ == c)
			ptr = (char*)s - 1;
	}
	return (c == '\0' ? (char*)s : ptr);
}
