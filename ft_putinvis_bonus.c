#include "libft.h"

void	ft_putinvis(char *str)
{
	size_t	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] <= 13 && str[i] >= 9)
			write(1, "$", 1);
		else
			write(1, &str[i], 1);
		i++;
	}
}
