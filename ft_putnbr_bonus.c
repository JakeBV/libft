#include "libft.h"

void	ft_putnbr(int n)
{
	char d;

	if (n < 0)
	{
		if (n <= -10)
			ft_putnbr(n / 10);
		else
			write(1, "-", 1);
		d = n % 10 * -1 + '0';
		write(1, &d, 1);
	}
	else
	{
		d = n % 10 + '0';
		if (n >= 10)
			ft_putnbr(n / 10);
		write(1, &d, 1);
	}
}
